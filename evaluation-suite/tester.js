const assert = require('assert');
const devduo = require('../devduo-ai-engine');

async function runTests() {
  console.log("🧪 Running Evaluation Suite...");

  try {
    console.log("- Testing AI Engine initialization...");
    await devduo.init('LITE');
    assert.strictEqual(devduo.status, 'READY');

    console.log("- Testing Code Generation...");
    const res = await devduo.generateCode("test");
    assert.ok(res.generated.includes("Generated by DevDuo"));
    assert.ok(res.tokensUsed > 0);

    console.log("✅ All tests passed.");
  } catch (e) {
    console.error("❌ Test failed:", e.message);
    process.exit(1);
  }
}

runTests();
